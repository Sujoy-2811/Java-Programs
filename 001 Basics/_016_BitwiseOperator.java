public class _016_BitwiseOperator {
    public static void main(String[] args) {
        int x = 3, y = 6;
        System.out.println(x & y);
        System.out.println(x | y);
        System.out.println(x ^ y);
        System.out.println(~x);
        System.out.println(x << 3);
        System.out.println(x >> 1);

    }
}
