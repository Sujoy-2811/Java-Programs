public class RelationalOperator {
    public static void main(String[] args) {
        int x = 10, y = 20;
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(y < x);
        System.out.println(y > x);
        System.out.println(x <= y);
        System.out.println(x >= y);
    }
}
